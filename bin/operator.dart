void main(List<String> args) {
  // Operasi Matematika
  var a = 4;
  var b = 10;
  var c = a + b;

  // Penjumlahan
  var penjumlahan = a + b;
  print(penjumlahan);
  // Pengurangan
  var pengurangan = a - b;
  print(pengurangan);
  // Perkalian
  var perkalian = a * b;
  print(perkalian);
  // Pebagian
  var pembagian = a / b;
  print(pembagian);
  // Bagi sisa (Modulus)
  var bagisisa = a % b;
  print(bagisisa);

  // Equality & Relational Operators
  // Lebih Besar Dari
  print(a > b);
  // Lebih Kurang Dari
  print(a < b);
  // Sama Dengan Dari
  print(a == b);
  // Tidak Sama Dengan
  print(a != b);
  // Lebih Besar Atau Sama Dengan
  print(a >= b);
  // Lebih Kecil Atau SAma Dengan
  print(a <= b);

  // Logical Operators
  bool x = true;
  bool y = false;

  // && AND => Bernilai False Apabila Salah Satu Nilai (False)
  print(x && y);
  // || OR => Bernilai True Apabila Salah Satu Nilai (True)
  print(x || y);
  // ! NOT => Nilai Menjadi Berlawanan
  print(x || y);
}
